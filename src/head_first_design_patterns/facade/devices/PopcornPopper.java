package head_first_design_patterns.facade.devices;

public class PopcornPopper {

    public void on(){
        System.out.println("PopcornPopper is on");
    }

    public void off(){
        System.out.println("PopcornPopper is off");
    }

    public void pop(){
        System.out.println("PopcornPopper popped");
    }
}
